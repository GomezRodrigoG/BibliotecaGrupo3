    private void jbBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbBuscarActionPerformed
        // TODO add your handling code here:
        try{
            int dni=Integer.parseInt(jtDni.getText());
            Lector lector = lectorData.buscarLector(dni);
            if(lector!=null){
                jtApellido.setText(lector.getApellido());
                jtNombre.setText(lector.getNombre());
                jtDireccion.setText(lector.getDireccion());
                jchEstado.setSelected(lector.isEstado());
                if(lector.isEstado()){
                    jbAlta.setEnabled(false);
                    jbBaja.setEnabled(true);
                }else{
                    jbBaja.setEnabled(false);
                    jbAlta.setEnabled(true);
                }
            }
            else{
                JOptionPane.showMessageDialog(null, "El dni ingresado no existe");
            }
        }catch(NumberFormatException co){
            JOptionPane.showMessageDialog(this, "Error al ingresar el dni");
        }
    }//GEN-LAST:event_jbBuscarActionPerformed

    private void jbBajaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbBajaActionPerformed
        // TODO add your handling code here:
        try{
            int dni=Integer.parseInt(jtDni.getText());
            try {
                lectorData.desactivarLector(dni);
            } catch (ClassNotFoundException ex) {
               JOptionPane.showMessageDialog(this, "Error al ingresar el dni "+ex.getMessage());
            }
        }catch(SQLException |NumberFormatException ex){
            JOptionPane.showMessageDialog(this, "Ingrese un dni valido "+ex.getMessage());
        } 
    }//GEN-LAST:event_jbBajaActionPerformed

    private void jbActualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbActualizarActionPerformed
        // TODO add your handling code here:
        try{
            int dni = Integer.parseInt(jtDni.getText());
            String nombre = jtNombre.getText();
            String apellido = jtApellido.getText();
            String direccion = jtDireccion.getText();
            if(nombre.equals("")||(apellido.equals(""))||(direccion.equals(""))){
                JOptionPane.showMessageDialog(this, "Nombre, direccion o Apellido se encuentran vacios");
            }
            else{
                Lector lector = new Lector(dni, direccion, apellido, nombre, true);
                lectorData.modificarLector(lector);
            }
        }catch(NumberFormatException ex){
            JOptionPane.showMessageDialog(this, "dni ingresado incorrectamente");
        }

    }//GEN-LAST:event_jbActualizarActionPerformed

