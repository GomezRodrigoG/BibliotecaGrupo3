    private void jbGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbGuardarActionPerformed
        // TODO add your handling code here:
        try{
            String dni = jtDni.getText();
            String nombre = jtNombre.getText();
            String apellido = jtApellido.getText();
            String direccion = jtDireccion.getText();
            // se controla que los campos de apellido, nombre y direccion no esten vacios
            if(nombre.equals("")||(apellido.equals(""))||(direccion.equals(""))||(dni.equals(""))){
                JOptionPane.showMessageDialog(this, "Hay campos vacios, todos son obligatorios!");
            }
            else{
                int DNI = Integer.parseInt(dni);
                Lector lector = new Lector(DNI, direccion, apellido, nombre, true);
                lectorData.guardarLector(lector);
            }
        }catch(NumberFormatException nf){
            JOptionPane.showMessageDialog(this, "Se ingreso legajo incorrecto");
        }
    }//GEN-LAST:event_jbGuardarActionPerformed

